    private void jBInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInserirActionPerformed
        FuncionarioBean funcionarioBean = new FuncionarioBean();
        
        dateUtil = new DateUtil();
        
        funcionarioBean.setNome(jTFNome.getText());
        funcionarioBean.setCpf(jFTFCpf.getText());
        try {
            funcionarioBean.setDataDeNascimento(dateUtil.converteDateParaCalendar(sdf2.parse(jTFDataDeNasc.getText())));
        } catch (ParseException ex) {
            Logger.getLogger(FormCadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        funcionarioBean.setDataDeCadastro(Calendar.getInstance());
        funcionarioBean.setTelefone(jFTFTelefone.getText());
        funcionarioBean.setEmail(jTFEmail.getText());
        funcionarioBean.setEndereco(jTFEndereco.getText());
        funcionarioBean.setSalario(Float.parseFloat(jTFSalario.getText()));
        
        
        
        if(funcionarioDao.inserir(funcionarioBean)!=null)
            JOptionPane.showMessageDialog(null, "Funcionario Inserido com Sucesso!");
        
    }//GEN-LAST:event_jBInserirActionPerformed

    private void jBSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSelecionarActionPerformed
        Integer codigo = Integer.parseInt(JOptionPane.showInputDialog("Digite o codigo do funcionario:"));
        
        FuncionarioBean funcionarioBean = funcionarioDao.selecionar(codigo);
        if(funcionarioBean!=null){
            jTFCodigo.setText(funcionarioBean.getCodigo().toString());
            jTFNome.setText(funcionarioBean.getNome());
            jFTFCpf.setText(funcionarioBean.getCpf());
            jTFDataDeNasc.setText(FormatUtil.formataDataDeNascimento(funcionarioBean.getDataDeNascimento()));
            jFTFTelefone.setText(funcionarioBean.getTelefone());
            jTFEmail.setText(funcionarioBean.getEmail());
            jTFEndereco.setText(funcionarioBean.getEndereco());
            jTFSalario.setText(funcionarioBean.getSalario().toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "Funcionario Não Encontrado");
            limpar();
            jTFNome.grabFocus();
        }
    }//GEN-LAST:event_jBSelecionarActionPerformed

    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        if(!"".equals(jTFCodigo.getText())){
            FuncionarioBean funcionarioBean = new FuncionarioBean();
        
            dateUtil = new DateUtil();

            funcionarioBean.setCodigo(Integer.parseInt(jTFCodigo.getText()));
            funcionarioBean.setNome(jTFNome.getText());
            funcionarioBean.setCpf(jFTFCpf.getText());
            try {
                funcionarioBean.setDataDeNascimento(dateUtil.converteDateParaCalendar(sdf2.parse(jTFDataDeNasc.getText())));
            } catch (ParseException ex) {
                Logger.getLogger(FormCadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
            funcionarioBean.setDataDeCadastro(Calendar.getInstance());
            funcionarioBean.setTelefone(jFTFTelefone.getText());
            funcionarioBean.setEmail(jTFEmail.getText());
            funcionarioBean.setEndereco(jTFEndereco.getText());
            funcionarioBean.setSalario(Float.parseFloat(jTFSalario.getText()));
            
            if (JOptionPane.showConfirmDialog(null, "Deseja alterar o Funcionario "+funcionarioBean.getNome()+" ?") == 0) {
                funcionarioDao.alterar(funcionarioBean);
            }else{}
        }
        else
            JOptionPane.showMessageDialog(null, "Selecione um Funcionario");
    }//GEN-LAST:event_jBAlterarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        if(!"".equals(jTFCodigo.getText())){
            Integer codigo = Integer.parseInt(jTFCodigo.getText());
            
            funcionarioDao.excluir(codigo);
            JOptionPane.showMessageDialog(null, "Funcionario "+jTFNome.getText()+" removido!");
            limpar();
            jTFNome.grabFocus();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um Funcionario");
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

