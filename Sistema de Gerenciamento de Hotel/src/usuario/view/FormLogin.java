    public void logar(){
        String login = jTFLogin.getText();
        UsuarioBean usuarioBean = null;
        ArrayList<UsuarioBean> usuarios = (ArrayList<UsuarioBean>) usuarioDao.logar(login);
        if(!usuarios.isEmpty()){
            usuarioBean = usuarios.get(0);
        }
        if(usuarioBean != null){
            if(usuarioBean.getSenha().equals(Criptografia.md5(jPFSenha.getText()))){
                dispose();
                new FormPrincipal().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Senha inv치lida!");
                limpar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Login inv치lido");
            limpar();
        }
    }
    
    private void limpar(){
        jTFLogin.setText("");
        jPFSenha.setText("");
        jTFLogin.grabFocus();
    }
    
